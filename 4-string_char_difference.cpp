#include <iostream>
#include <string>

int main()
{
    // This is a string example using "double quotes" (").

    std::string myString = "Hello, World!";

    // This is a character example using 'single quotes' ('').

    char myChar = 'A';

    // Print the values
    std::cout << "String: " << myString << std::endl;
    std::cout << "Char: " << myChar << std::endl;

    return 0;
}
